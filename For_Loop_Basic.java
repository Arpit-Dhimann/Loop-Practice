// Problem: Print numbers from 1 to 10.
// Solution: Used a 'for' loop to iterate from 1 to 10 and print each value.

public class For_Loop_Basic {
    public static void main(String[] args) {
        
        // Simple for loop to print numbers from 1 to 10
        for (int i = 1; i <= 10; i++) {
            
            System.out.println("Current count: " + i);
            
        }
    }
}